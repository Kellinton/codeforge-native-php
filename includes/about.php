    // Incrementando as barra-sobreras de progresso no reload da página
    window.addEventListener('load', function() {
      var stats = document.querySelectorAll('.estatistica-sobre');
      stats.forEach(function(stat) {
        var barra = stat.querySelector('.barra-sobre');
        var number = stat.querySelector('.numero-sobre');
        var width = parseInt(stat.dataset.percent); // Converter para número inteiro
        var currentWidth = 0;
        var currentNumber = 0;
        var increment = width / 100; // Calcular incremento para suavizar a transição
        var interval = setInterval(function() {
          currentWidth += increment;
          currentNumber++;
          barra.style.width = currentWidth + '%'; // Atualizar largura da barra
          number.innerText = currentNumber + '%'; // Atualizar texto do número
          if (currentNumber >= width) {
            clearInterval(interval); // Parar o intervalo quando alcançar o valor final
          }
        }, 50); // Definir intervalo para cada incremento
      });
    });



    document.addEventListener("DOMContentLoaded", function() {
    var menuItems = document.querySelectorAll(".naccs .menu div");
    var listItems = document.querySelectorAll(".naccs ul li");

    menuItems.forEach(function(menuItem, index) {
        menuItem.addEventListener("click", function() {
            // Remove a classe 'active' de todos os elementos do menu
            menuItems.forEach(function(item) {
                item.classList.remove("active");
            });
            // Remove a classe 'active' de todos os elementos da lista
            listItems.forEach(function(item) {
                item.classList.remove("active");
            });

            // Adiciona a classe 'active' ao elemento clicado do menu
            this.classList.add("active");
            // Adiciona a classe 'active' ao elemento correspondente na lista
            listItems[index].classList.add("active");

            // Ajusta a altura da lista de acordo com o item ativo
            var listItemHeight = listItems[index].offsetHeight;
            document.querySelector(".naccs ul").style.height = listItemHeight + "px";
        });
    });
});
